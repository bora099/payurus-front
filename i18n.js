// Get current language from localStorage or default to 'en'
let currentLang = localStorage.getItem('language') || 'en';

// Function to change language
function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updatePageContent();
    updateLanguageButton();
}

// Function to update all translatable content
function updatePageContent() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLang][key];
            } else if (element.tagName === 'A' || element.tagName === 'BUTTON') {
                // For links and buttons, update text content
                const text = translations[currentLang][key];
                if (text) {
                    element.textContent = text;
                }
            } else if (key === 'hero-title') {
                // Special handling for hero-title with HTML content
                element.innerHTML = translations[currentLang][key];
            } else {
                element.textContent = translations[currentLang][key];
            }
        }
    });
    
    // Update title if exists
    const titleElement = document.querySelector('[data-i18n-title]');
    if (titleElement) {
        const titleKey = titleElement.getAttribute('data-i18n-title');
        if (translations[currentLang] && translations[currentLang][titleKey]) {
            document.title = `Payurus - ${translations[currentLang][titleKey]}`;
        }
    }
    
    // Special handling for signup privacy and terms text (different sentence structure in TR)
    const privacyContainer = document.querySelector('[data-i18n="signup-privacy"]')?.parentElement;
    const termsContainer = document.querySelector('[data-i18n="signup-terms"]')?.parentElement;
    
    if (privacyContainer) {
        const privacyPrefix = privacyContainer.querySelector('[data-i18n="signup-privacy"]');
        const privacyLink = privacyContainer.querySelector('[data-i18n="signup-privacy-link"]');
        const privacyText = privacyContainer.querySelector('[data-i18n="signup-privacy-text"]');
        
        if (currentLang === 'tr') {
            // Turkish: "KVKK metnini okudum ve onaylıyorum"
            if (privacyPrefix) privacyPrefix.style.display = 'none';
            if (privacyLink && translations[currentLang]['signup-privacy-link']) {
                privacyLink.textContent = translations[currentLang]['signup-privacy-link'];
            }
            if (privacyText && translations[currentLang]['signup-privacy-text']) {
                privacyText.textContent = translations[currentLang]['signup-privacy-text'];
                privacyText.style.display = 'inline';
            }
        } else {
            // English: "I have read and accept the Privacy Policy."
            if (privacyPrefix && translations[currentLang]['signup-privacy']) {
                privacyPrefix.textContent = translations[currentLang]['signup-privacy'];
                privacyPrefix.style.display = 'inline';
            }
            if (privacyLink && translations[currentLang]['signup-privacy-link']) {
                privacyLink.textContent = translations[currentLang]['signup-privacy-link'];
            }
            if (privacyText) {
                privacyText.textContent = '.';
                privacyText.style.display = 'inline';
            }
        }
    }
    
    if (termsContainer) {
        const termsPrefix = termsContainer.querySelector('[data-i18n="signup-terms"]');
        const termsLink = termsContainer.querySelector('[data-i18n="signup-terms-link"]');
        const termsText = termsContainer.querySelector('[data-i18n="signup-terms-text"]');
        
        if (currentLang === 'tr') {
            // Turkish: "Kullanım koşullarını kabul ediyorum"
            if (termsPrefix) termsPrefix.style.display = 'none';
            if (termsLink && translations[currentLang]['signup-terms-link']) {
                termsLink.textContent = translations[currentLang]['signup-terms-link'];
            }
            if (termsText && translations[currentLang]['signup-terms-text']) {
                termsText.textContent = translations[currentLang]['signup-terms-text'];
                termsText.style.display = 'inline';
            }
        } else {
            // English: "I accept the Terms of Use."
            if (termsPrefix && translations[currentLang]['signup-terms']) {
                termsPrefix.textContent = translations[currentLang]['signup-terms'];
                termsPrefix.style.display = 'inline';
            }
            if (termsLink && translations[currentLang]['signup-terms-link']) {
                termsLink.textContent = translations[currentLang]['signup-terms-link'];
            }
            if (termsText) {
                termsText.textContent = '.';
                termsText.style.display = 'inline';
            }
        }
    }
}

// Function to update language button
function updateLanguageButton() {
    const langButtons = document.querySelectorAll('.lang-btn');
    langButtons.forEach(btn => {
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
            btn.classList.remove('opacity-50');
        } else {
            btn.classList.remove('active');
            btn.classList.add('opacity-50');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    // Load saved language from localStorage
    const savedLang = localStorage.getItem('language');
    if (savedLang && (savedLang === 'en' || savedLang === 'tr')) {
        currentLang = savedLang;
    }
    document.documentElement.lang = currentLang;
    updatePageContent();
    updateLanguageButton();
});

